                            <p align="justify">Dalam rangka penuntasan Program Wajib Belajar 9 Tahun, Pemerintah
                                melaksanakan Program Block Grant Unit Sekolah Baru (USB) SMP dan Pengembangan
                                SD-SMP Satu Atap, melalui Direktorat Pembinaan SMP, Direktorat Jenderal Pendidikan
                                Dasar, dan Kementerian Pendidikan Nasional, maka unit sekolah baru dengan nama
                                SMP Negeri 3 Nekamese dibangun sejak tanggal 29 Pebruari 2012 berdasarkan Surat
                                Keputusan Bupati Kupang No : 349/KEP/HK/2011 tentang Pembukaan Unit Sekolah
                                Baru SMP. Pengembangan SD –SMP satu atap tanggal 15 Agustus 2011 dan sudah mulai
                                beroperasi pada tahun pelajaran 2012-2013 berlanjut hingga saat ini. <br><br>
                                SMPN 6 Nekamese ini beroperasi dari tahun 2012-2013 Pada nomenklatur
                                SMPN 3 Nekamese dipimpin oleh Bapak Plt Yoskar Passu, S.Pd, kemudian diganti
                                dengan kepala sekolah baru yaitu ibu Juliety Banoet, S.Pd. Pada tahun 2016 terjadi
                                perubahan nomenklatur menjadi SMP Negeri 6 Nekamese dipimpin oleh ibu Yulianti
                                Pulungtana, S.Pd dan saat ini pada tahun 2022 mengalami pergantian kepala sekolah
                                yaitu ibu Lidyasih Widyanti, S.Pd. <br><br>

                                Prestasi sekolah ditorehkan baik oleh peserta didik dan para pendidik dengan
                                sangat membanggakan sejak sekolah ini beroperasi. Tahun 2015 menerima piagam
                                penghargaan dari Menteri Pendidikan dan Kebudyaan Republik Indonesia sebagai
                                sekolah dengan Indeks Integras penyelenggaraan Ujian Nasional yang tinggi pada
                                tahun 2015. Pada tahun berikutnya hingga saat ini program Gerakan Literasi Sekolah
                                digalakkan untuk mendukung terciptanya SDM yang Cerdas, Berkarakter Bangsa,
                                Kompetitif berlandaskan IMTAQ dan GLS ini banyak menghasilkan karya yang
                                membanggakan dari peserta didik dan para pendidik di SMPN 6 Nekamese. <br>

                                Buku karya peserta didik yang ber-ISBN yaitu : <br>
                                1. Terimakasih Untuk Cintamu dengan ISBN 978-623-7760-09-2 <br>
                                2. Antologi Puisi berjudul Gelita Durja Pandemi” Sebuah Elegi” dengan ISBN
                                978-623-298-693-0. <br>
                                Sedangkan buku Karya pendidik antara lain : <br>
                                1. Kuingin”Goresan Cinta Dalam Daifan” dengan ISBN 789-602-74718-9-4 <br>
                                2. Antologi Puisi dengan judul Bumantara Cinta dengan ISBN 978-602-74718-8-7 <br>
                                dan beberapa karya tulis lainnya serta prestasi peserta didik di berbagai perlombaan
                                baik dibidang olahraga maupun akademik. <br>
                                SMP Negeri 6 Nekamese menggunakan standar pelayanan pendidikan dengan
                                mengacu pada kurikulum 13 untuk saat ini namun tetap mempersiapkan diri dalam
                                pelaksanaan Kurikulum Merdeka pada tahun ajaran mendatang. <br><br>
                                Demikian sekilas tentang SMP Negeri 6 Nekamese dari 2011 hingga saat ini.</p>
